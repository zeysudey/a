#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	number;

	number = 'a';
	while (number <= 'z')
	{
		ft_putchar(number);
		number++;
	}
}
